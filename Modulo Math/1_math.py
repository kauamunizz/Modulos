import math

# Numero de Pi
print(math.pi)

# Numero de Euler
print(math.e)

# Arredondar para cima ou para baixo
num = 6.7
print(math.ceil(num))
print(math.floor(num))

# Fatorial
num2 = int(input("Digite um numero: \n"))
print(math.factorial(num2))

# Pontencia
print(math.pow(5, 5))

# Raiz quadrada
print(math.sqrt(9))

# logaritimo
print(math.log(10))